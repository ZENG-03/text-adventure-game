"""
正则表达式测试模块

该模块用于测试和验证场景选项解析的正则表达式模式。
主要用于开发调试阶段，确保选项文本、目标场景和条件能够被正确解析。

测试用例：
    解析包含条件表达式的复杂选项行，验证正则表达式的匹配效果。
"""

import re


def test_option_regex():
    """
    测试选项解析正则表达式
    
    该函数测试用于解析场景选项的正则表达式模式。
    选项格式：- 选项文本 [前往 目标场景] [条件：条件表达式]
    
    测试内容：
        - 验证选项文本提取
        - 验证目标场景提取
        - 验证条件表达式提取
    
    Returns:
        None
    
    Example:
        输入行："- 按含义放入收集到的关键信物 [前往 final_test_1_correct] [条件：js:...]"
        期望输出：('按含义放入收集到的关键信物', 'final_test_1_correct', "js:...")
    """
    line = (
        " - 按含义放入收集到的关键信物（只要收集到至少5个便可开启） "
        "[前往 final_test_1_correct] "
        "[条件：js:['星盘钥匙','机械齿轮','共鸣水晶','神秘颜料','生命之露',"
        "'符文石','夜莺胸针','银手镯','主人的怀表'].filter(i=>hasItem(i)).length >= 5]"
    )
    
    pattern = r'\-\s*(.*?)\s*\[前往\s+(.*?)\]\s*(?:\[条件：(.*?)\])?'

    match = re.match(pattern, line.strip())
    
    if match:
        print(match.groups())


if __name__ == '__main__':
    test_option_regex()

